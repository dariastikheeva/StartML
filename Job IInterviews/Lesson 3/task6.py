def color_sort(nums):
    """Сортирует массив nums на месте, чтобы все элементы одного цвета были соседними, а цвета шли в порядке "красный, белый, синий".
    """

    # Инициализируем три указателя:
    # - low указывает на начало массива (область для 0)
    # - high указывает на конец массива (область для 2)
    # - i используется для итерации по массиву
    low = 0
    high = len(nums) - 1
    i = 0

    # Итерируем по массиву, пока i не превысит high
    while i <= high:
        # Если текущий элемент равен 0, меняем его местами с элементом в low, увеличиваем i и low
        if nums[i] == 0:
            nums[i], nums[low] = nums[low], nums[i]
            i += 1
            low += 1
        # Если текущий элемент равен 1, просто увеличиваем i
        elif nums[i] == 1:
            i += 1
        # Если текущий элемент равен 2, меняем его местами с элементом в high, уменьшаем high
        else:
            nums[i], nums[high] = nums[high], nums[i]
            high -= 1

    # Возвращаем отсортированный массив
    return nums